def split_row(line: str) -> list[str]:
    return [part.strip() for part in line.strip().strip("|").split("|")]


def load_plan() -> dict[str, dict[str, str]]:
    rows: dict[str, dict[str, str]] = {}
    in_table = False
    headers: list[str] = []
    for line in PLAN.read_text(encoding="utf-8").splitlines():
        if line.startswith("## 5. 全量分型明细"):
            in_table = True
            continue
        if not in_table or not line.startswith("|") or line.startswith("|---"):
            continue
        parts = split_row(line)
        if parts and parts[0] == "NBD ID":
            headers = parts
            continue
        if len(parts) == len(headers) and parts[0].startswith("NBD"):
            rows[parts[0]] = dict(zip(headers, parts))
    return rows


