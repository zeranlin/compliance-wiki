def fixture_body(title: str, nbd_type: str, verdict: str, text: str) -> str:
    return (
        f"# {title} fixture\n\n"
        "## 待审片段\n"
        f"{text}\n\n"
        "## 期望\n"
        f"小模型应输出：{verdict}。\n"
    )


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--ids", help="comma-separated NBD ids; defaults to the historical Batch 3 ids")
    args = parser.parse_args()
    ids = [item.strip() for item in args.ids.split(",") if item.strip()] if args.ids else BATCH3
    plan = load_plan()
    for nbd_id in ids:
        row = plan[nbd_id]
        title = row["审查点"]
        nbd_type = row["类型"]
        positive, negative, boundary = overrides(nbd_id, title) or common_docs(title, nbd_type)
        out_dir = FIXTURES / nbd_id
        out_dir.mkdir(parents=True, exist_ok=True)
        fixtures = [
            ("positive", "命中", positive),
            ("negative", "不命中", negative),
            ("boundary", "待人工复核", boundary),
        ]
        for slug, verdict, text in fixtures:
            path = out_dir / f"{slug}-{clean_slug(nbd_id)}.md"
            content = front_matter(nbd_id, slug, verdict) + fixture_body(title, nbd_type, verdict, text)
            path.write_text(content, encoding="utf-8")
            print(path.relative_to(ROOT))


if __name__ == "__main__":
    main()
